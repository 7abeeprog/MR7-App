st.divider()
st.subheader("🤖 وكيل التوصيات الذكي")

interest = st.selectbox(
    "أخبرني باهتمامك الحالي لنقترح لك المسار المناسب:",
    ["اختر مجالاً...", "القيادة وبناء الفرق", "التسويق والمبيعات", "التقنية والبرمجة"]
)

if interest == "القيادة وبناء الفرق":
    st.success("🎯 توصية: ننصحك بالبدء في 'مسار القيادة - المستوى الأول'. ستتعلم كيف تدير فريقاً من 10 أشخاص.")
elif interest == "التسويق والمبيعات":
    st.success("🎯 توصية: 'دورة التسويق بالعمولة' هي الأنسب لك الآن.")
elif interest == "التقنية والبرمجة":
    st.success("🎯 توصية: ننصحك بمسار 'أساسيات الذكاء الاصطناعي'.")
