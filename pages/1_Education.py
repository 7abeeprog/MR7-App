import streamlit as st

# 1. جدار الحماية: التأكد من أن المستخدم سجل دخوله أولاً
if 'logged_in' not in st.session_state or st.session_state['logged_in'] == False:
    st.warning("الرجاء تسجيل الدخول من الصفحة الرئيسية أولاً.")
    st.stop() # يمنع تشغيل باقي الكود إذا لم يكن مسجلاً

# 2. محتوى قسم التعليم
st.title("🎓 النظام التعليمي التفاعلي")
