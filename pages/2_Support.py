import streamlit as st

# 1. جدار الحماية (التأكد من تسجيل الدخول)
if 'logged_in' not in st.session_state or st.session_state['logged_in'] == False:
    st.warning("الرجاء تسجيل الدخول من الصفحة الرئيسية أولاً.")
    st.stop()

# 2. واجهة المستخدم
st.title("💬 مركز الدعم الفني الذكي")
st.write("نحن هنا لمساعدتك. صف مشكلتك وسيقوم وكيلنا الذكي بتوجيهها للقسم المختص فوراً.")

st.divider()

problem_text = st.text_area("اكتب تفاصيل المشكلة هنا (مثال: واجهت خطأ في التطبيق، أو رصيدي لم يظهر):", height=150)

if st.button("إرسال التذكرة"):
    if problem_text:
        # -----------------------------------------
        # 🤖 عقل وكيل الدعم الفني (تحليل النصوص)
        # -----------------------------------------
        with st.spinner("الوكيل يقوم بتحليل المشكلة..."):
            
            # محاكاة لعمل الذكاء الاصطناعي في تحليل الكلمات المفتاحية
            category = "عامة 📝" # التصنيف الافتراضي
            
            # نبحث عن كلمات تدل على المال
            if any(word in problem_text for word in ["دفع", "فلوس", "محفظة", "رصيد", "سحب", "دولار", "شراء"]):
                category = "مالية 💰"
            # نبحث عن كلمات تدل على التعليم
            elif any(word in problem_text for word in ["دورة", "شهادة", "درس", "اختبار", "مسار", "فيديو"]):
                category = "تعليمية 🎓"
            # نبحث عن كلمات تدل على أعطال تقنية
            elif any(word in problem_text for word in ["تطبيق", "بطيء", "شاشة", "حساب", "تسجيل", "خطأ", "لا يعمل"]):
                category = "تقنية ⚙️"
        
        # 3. عرض النتيجة
        st.success("تم استلام تذكرتك بنجاح وتوثيقها في النظام!")
        st.info(f"🤖 تصنيف الوكيل الذكي: تم توجيه تذكرتك إلى قسم **( المشاكل الـ{category} )**")
        
    else:
        st.warning("الرجاء كتابة المشكلة أولاً قبل الإرسال.")
